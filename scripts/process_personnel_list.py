added = []
deleted = []
updated = []
unchanged = []
skipped = []

lines = [l.strip() for l in raw_data.strip().split('\n') if l.strip()]

for line in lines:
    line = re.sub(r'\s+', ' ', line) # Normalize spaces
    
    # CASE 1: EKLENECEK prefix
    if line.startswith("EKLENECEK EKLENECEK"):
        name = line.replace("EKLENECEK EKLENECEK", "").strip()
        added.append(name)
        continue
    
    # CASE 2: SİLİNECEK suffix
    if line.endswith(" SİLİNECEK"):
        # Pattern: [NAME] [NAME] SİLİNECEK
        # The content before SİLİNECEK is the name repeated twice.
        content = line.replace(" SİLİNECEK", "")
        words = content.split(' ')
        mid = len(words) // 2
        
        # Heuristic: split exactly in half
        part1 = " ".join(words[:mid])
        part2 = " ".join(words[mid:])
        
        # Check if actually repeated
        # If uneven words, mid splits differently.
        # Try to find best split manually if strict half doesn't work (e.g. if name length is odd but repetition logic holds?)
        # Actually usually it is exact repetition.
        deleted.append(part1)
        continue

    # CASE Special: SİLİNECEK SİLİNECEK [NAME] (one outlier in data: "SİLİNECEK SİLİNECEK MAHMOUD KOURANI")
    if line.startswith("SİLİNECEK SİLİNECEK"):
        name = line.replace("SİLİNECEK SİLİNECEK", "").strip()
        deleted.append(name)
        continue
        
    # CASE 3 & 4: UPDATE OR UNCHANGED
    # Pattern: [OLD] [OLD] [NEW]
    # We need to find the split point.
    words = line.split(' ')
    
    # We iterate possible split points for the first repeating segment
    # The name is likely max 5 words.
    found = False
    for n in range(1, len(words) // 2 + 1):
        # A = words[0:n]
        # B = words[n:2*n]
        # Check if A == B
        A = words[0:n]
        B = words[n:2*n]
        
        # Allow loose match? The user says "Column 1 - Column 2".
        # Sometimes there are typos in col 2? "Eğer sol sütunlarda 'EKLENECEK' yazıyorsa..."
        # "DÜZELTİLECEK Kayıtlar: Eğer sağ sütunda ne 'SİLİNECEK' ne de 'EKLENECEK' yazıyor, ancak sol sütundaki isimle sağ sütundaki isim arasında yazım farkı varsa"
        
        if A == B:
            # Found the repetitive part!
            old_name = " ".join(A)
            new_name = " ".join(words[2*n:])
            
            if not new_name:
                continue # Try larger n? Or maybe malformed
            
            # Normalize for comparison
            if old_name.replace(" ", "") == new_name.replace(" ", ""):
                # UNCHANGED (ignoring space diffs for now, or strict?)
                # User says: "AYNI KALANLAR: Eğer iki taraftaki isim de tamamen aynıysa işlem yapma"
                if old_name == new_name:
                     unchanged.append(old_name)
                else: 
                     # Space diff counts as update per request "harf, boşluk, karakter"
                     updated.append((old_name, new_name))
            else:
                updated.append((old_name, new_name))
            
            found = True
            break
            
    if not found:
        # Fallback: maybe just assume split at 1/3?
        # Many names are 2 words. Total 6 words -> 2, 2, 2.
        # But "MOHAMED MOHAMED MOHAMED KAMEL ELBANA" -> 4 words?
        # Let's inspect complex ones manually or log them
        # One complex case: "MUHAMMED KAMEL ELBANA MUHAMMED KAMEL ELBANA MOHAMED MOHAMED MOHAMED KAMEL ELBANA"
        # Wait, that example in text: 
        # "MUHAMMED KAMEL ELBANA MUHAMMED KAMEL ELBANA MOHAMED MOHAMED MOHAMED KAMEL ELBANA"
        # A = MUHAMMED KAMEL ELBANA (3)
        # B = MUHAMMED KAMEL ELBANA (3)
        # C = MOHAMED MOHAMED MOHAMED KAMEL ELBANA (5)
        # 3+3 = 6 words. Total words = 11?
        skipped.append(line)


with open('personnel_processing_results.txt', 'w', encoding='utf-8') as f:
    f.write("-" * 30 + "\n")
    f.write(f"EKLENECEK ({len(added)}):\n")
    for x in added: f.write(f" - {x}\n")

    f.write("\n" + "-" * 30 + "\n")
    f.write(f"SİLİNECEK ({len(deleted)}):\n")
    for x in deleted: f.write(f" - {x}\n")

    f.write("\n" + "-" * 30 + "\n")
    f.write(f"DÜZELTİLECEK ({len(updated)}):\n")
    for old, new in updated: f.write(f" - {old} -> {new}\n")

    f.write("\n" + "-" * 30 + "\n")
    f.write(f"AYNI/ATLANAN ({len(unchanged)}):\n")
    # for x in unchanged: f.write(f" - {x}\n")

    f.write("\n" + "-" * 30 + "\n")
    f.write(f"PARSING HATASI / BELİRSİZ ({len(skipped)}):\n")
    for x in skipped: f.write(f" - {x}\n")
    
print("Results written to personnel_processing_results.txt")

